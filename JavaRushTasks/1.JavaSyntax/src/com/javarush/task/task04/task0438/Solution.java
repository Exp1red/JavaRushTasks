/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) {
        int i = 1, b = 1;
        for ( i = 1; i<=11 ; i++){
            while (b<=9) {
                System.out.print("8");
                b++;
            }

            System.out.println("8");

        }

    }
}
